
# 3. Operators

# Assign values to variables
a, b = 10, 3

# Arithmetic operators
print(a + b)   # Addition → 10 + 3 = 13
print(a - b)   # Subtraction → 10 - 3 = 7
print(a * b)   # Multiplication → 10 * 3 = 30
print(a / b)   # Division → 10 / 3 = 3.333... (always returns float)

# More arithmetic operators
print(a // b)  # Floor division → 10 // 3 = 3 (drops the decimal part)
print(a % b)   # Modulus → remainder of 10 ÷ 3 = 1
print(a ** b)  # Exponentiation → 10³ = 1000

# Comparison operators (results are Boolean: True or False)
print(a > b)   # Checks if a is greater than b → True
print(a == b)  # Checks if a is equal to b → False

# Logical operators
print(True and False)  # AND → True only if both are True
print(True or False)   # OR → True if at least one is True
print(not True)        # NOT → reverses the Boolean value

# Assignment operator
a += 5   # Same as: a = a + 5 → a becomes 15
print(a)

