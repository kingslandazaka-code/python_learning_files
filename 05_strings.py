
# 5. Strings
# Store a string in a variable
text = "Python Programming"

# String slicing
# text[0:6] means: start from index 0 up to (but not including) index 6
# Index positions: P=0 y=1 t=2 h=3 o=4 n=5
print(text[0:6])   # Output: Python

# Convert the entire string to lowercase
print(text.lower())   # Output: python programming

# Replace a word inside the string
# "Python" is replaced with "Java"
print(text.replace("Python", "Java"))   # Output: Java Programming

# Escape character \n creates a new line
print("Line1\nLine2")
# Output:
# Line1
# Line2

